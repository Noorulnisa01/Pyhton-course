# input function:

# input function is used to take input from the user.

a = input("Enter your name: ")
b = input("Enter your age: ")
c = input("Enter your city: ")

# if we want to print all the data in a single line then we can use the " + " symbol.

print(" My name is " + a + " \n my age is " + b + " \nI live in " + c)

# Here we use the " + " symbol for concatenation of strings.
# \n is used for new line.
# Concatenation means to join two or more strings.

# Output:
# My name is Noor \n my age is 20 \n I live in Delhi